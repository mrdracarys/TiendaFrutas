    private void GuardarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarCompraActionPerformed
        // TODO add your handling code here:
        DAOfrutas contFruta= ControldeFrutas.getInstance();
        String DirFru=TextDireccion.getText();
        String NomFru=TextNombre.getText();
        String TipoPago="";
        if(Ch_Efectivo.isSelected())
            TipoPago+="Efectivo ";
        if(Ch_Nequi.isSelected())
            TipoPago+="Nequi ";
        if(Ch_Tarjeta.isSelected())
            TipoPago+="Tarjeta ";
        if(Ch_Especie.isSelected())
            TipoPago+="Especie ";
        String estCan=spiEst.getValue().toString();
         String[] fruta={DirFru,NomFru,TipoPago,estCan};
        if(contFruta.GuardarFruta(fruta)){
           JOptionPane.showMessageDialog(this, "Guardado Exitoso"); 
        }
            
    }//GEN-LAST:event_GuardarCompraActionPerformed

